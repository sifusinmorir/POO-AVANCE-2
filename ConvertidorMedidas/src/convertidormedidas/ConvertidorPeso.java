package convertidormedidas;

public class ConvertidorPeso {
    public static double kilogramosAgramos(double kilogramos) {
        return kilogramos * 1000;
    }
    public static double librasAgramos(double libras) {
        return libras * 453.592;
    }
    public static double onzasAgramos(double onzas) {
        return onzas * 28.3495;
    }
}

